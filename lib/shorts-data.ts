// Helper functions
export function getShortById(id: string): ShortVideo | undefined {
  return shortsVideos.find(video => video.id === id)
}

export function getShortsByChannel(channel: string): ShortVideo[] {
  return shortsVideos.filter(video => video.channel === channel)
}

export function getYouTubeEmbedUrl(youtubeId: string): string {
  return `https://www.youtube.com/embed/${youtubeId}?autoplay=1&mute=1&loop=1&playlist=${youtubeId}&controls=0&modestbranding=1&rel=0&showinfo=0&fs=0&iv_load_policy=3&disablekb=1&playsinline=1`
}

